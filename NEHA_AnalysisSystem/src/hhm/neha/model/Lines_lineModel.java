package hhm.neha.model;

public class Lines_lineModel {
	public int id;
    public String name;
    public int point;
    
    public int getId(){
    	return id;
    }
    public void setId(int id){
    	this.id=id;
    }
    public String getName(){
    	return name;
    }
    public void setName(String name){
    	this.name=name;
    }
    public int getPoint(){
    	return point;
    }
    public void setPoint(int point){
    	this.point=point;
    }
}
